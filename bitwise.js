
// Functions for problem 1

function bitwiseAnd(x, y)
{
  return x & y;
}

function bitwiseOr(x, y)
{
  return x | y;
}

function bitwiseXor(x, y)
{
  return x ^ y;
}

function bitwiseLeftShift(x, y)
{
  return x << y;
}

function bitwiseRightShift(x, y)
{
  return x >> y;
}

